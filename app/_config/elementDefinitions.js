/**
 * Get locked properties for an element type
 * @param {string} elementType - The element type
 * @returns {object} - Object with locked prop values
 */
export function getLockedProps(elementType) {
  const element = elementDefinitions[elementType];
  if (!element) return {};

  const locked = {};
  element.lockedProps?.forEach((prop) => {
    locked[prop.name] = prop.value;
  });

  return locked;
}
