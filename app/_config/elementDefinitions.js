/**
 * Get element definition, optionally filtered for a template context.
 * When fromTemplate is set, only properties listed in templatePropertyMaps
 * are included. All other properties stay on the element but are hidden
 * from PropertyPanel and cannot be schema-toggled by the user.
 */
export function getElementDef(elementType, fromTemplate) {
  const def = elementDefinitions[elementType];
  if (!def) return null;
  if (!fromTemplate) return def;

  const map = templatePropertyMaps[fromTemplate]?.[elementType];
  if (!map) return def;

  return {
    ...def,
    contentProps: def.contentProps?.filter((p) => map.contentProps.includes(p.name)) || [],
    styleProps: def.styleProps?.filter((p) => map.styleProps.includes(p.name)) || [],
  };
}

/**
 * Helper function to get property definition
 * @param {string} elementType - The element type (e.g., 'heading', 'button')
 * @param {string} propertyName - The property name (e.g., 'fontSize', 'text')
 * @returns {object|null} - The property definition or null if not found
 */
export function getPropertyDef(elementType, propertyName) {
  const element = elementDefinitions[elementType];
  if (!element) return null;

  // Search in contentProps
  let prop = element.contentProps?.find((p) => p.name === propertyName);
  if (prop) return prop;

  // Search in styleProps
  prop = element.styleProps?.find((p) => p.name === propertyName);
  if (prop) return prop;

  return null;
}

/**
 * Get all properties for an element (both content and style)
 * @param {string} elementType - The element type
 * @returns {array} - Array of all property definitions
 */
export function getAllProperties(elementType) {
  const element = elementDefinitions[elementType];
  if (!element) return [];

  return [...(element.contentProps || []), ...(element.styleProps || [])];
}

/**
 * Get default props for an element type
 * @param {string} elementType - The element type
 * @returns {object} - Object with default prop values
 */
export function getDefaultProps(elementType) {
  const element = elementDefinitions[elementType];
  if (!element) return {};

  const defaults = {};
  element.contentProps?.forEach((prop) => {
    if (prop.default !== undefined && prop.default !== null) {
      defaults[prop.name] = prop.default;
    }
  });

  return defaults;
}

